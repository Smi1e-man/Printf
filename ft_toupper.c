#include "printf.h"

void	ft_toupper(char **s)
{
	int		i;

	i = 0;
	while (s[0][i] != '\0')
	{
		if (s[0][i] >= 97 && s[0][i] <= 122)
			s[0][i] = s[0][i] - 32;
		i++;
	}
}
